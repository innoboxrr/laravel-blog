<template>
	
	<form :id="formId" @submit.prevent="onSubmit">      

        <text-input-component
            label="TÃ­tulo"
            name="title"
            :required="true"
            :custom-class="inputClass"
            :disabled="disabled"
            v-model="post.title" />

        <button-component
            :custom-class="buttonClass"
            :disabled="disabled"
            value="Crear" />
        
    </form>

</template>

<script>

    import { createModel } from '@blogModels/blog-post'
    import JSValidator from 'innoboxrr-js-validator'
    import {
        TextInputComponent,
        ButtonComponent,
    } from 'innoboxrr-form-elements'
	
	export default {
        components: {
            TextInputComponent,
            ButtonComponent,
        },
        props: {
        	formId: {
        		type: String,
        		default: 'createBlogPostForm',
        	}
        },
        emits: ['submit', 'change'],
        data() {
            return {
                post: {
                    title: '',
                },
                disabled: false,
                JSValidator: undefined,
            }
        },
        mounted() {
            this.fetchData();
            this.JSValidator = new JSValidator(this.formId).init();
        },
        watch: {
            post: {
                handler() {
                    this.$emit('change', this.post);
                },
                deep: true,
            }
        },
        methods: {

            fetchData() {},

            onSubmit() {
                if(this.JSValidator.status) {
                    this.disabled = true;
                    createModel(this.post).then( res => {
                        this.$emit('submit', res);
                        setTimeout(() => { this.disabled = false; }, 2500);
                    }).catch(error => {
                        this.disabled = false;
                        if(error.response.status == 422)
                            this.JSValidator
                                .appendExternalErrors(error.response.data.errors);
                    });
                } else {
                    this.disabled = false;
                }
            }
        }
	}
</script>